package pojo;

public class Profile {
    String name;
    String surname;
    String nameInLatin;
    String surnameInLatin;
    String birthDate;
    String firstContactData;
    String secondContactData;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSurname() {
        return surname;
    }

    public void setSurname(String surname) {
        this.surname = surname;
    }

    public String getNameInLatin() {
        return nameInLatin;
    }

    public void setNameInLatin(String nameInLatin) {
        this.nameInLatin = nameInLatin;
    }

    public String getSurnameInLatin() {
        return surnameInLatin;
    }

    public void setSurnameInLatin(String surnameInLatin) {
        this.surnameInLatin = surnameInLatin;
    }

    public String getBirthDate() {
        return birthDate;
    }

    public void setBirthDate(String birthDate) {
        this.birthDate = birthDate;
    }

    public String getFirstContactData() {
        return firstContactData;
    }

    public void setFirstContactData(String firstContactData) {
        this.firstContactData = firstContactData;
    }

    public String getSecondContactData() {
        return secondContactData;
    }

    public void setSecondContactData(String secondContactData) {
        this.secondContactData = secondContactData;
    }

}
